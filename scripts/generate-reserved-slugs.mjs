import { mkdir, readdir, writeFile } from 'node:fs/promises';
import path from 'node:path';

const projectRoot = process.cwd();
const routesDirectory = path.join(projectRoot, 'src/routes');
const staticDirectory = path.join(projectRoot, 'static');
const outputPath = path.join(projectRoot, 'shared/generated/reservedSuccessRoomSlugs.ts');
const urlSlugPattern = /^[a-z0-9]+(?:-[a-z0-9]+)*$/;

const isUrlSlug = (name) => urlSlugPattern.test(name);

const getTopLevelRouteSlugs = async () => {
  const entries = await readdir(routesDirectory, { withFileTypes: true });

  return entries
    .filter((entry) => entry.isDirectory())
    .map((entry) => entry.name)
    // Route groups "(...)" and dynamic segments "[...]" fail the slug pattern.
    .filter(isUrlSlug);
};

const getTopLevelStaticSlugs = async () => {
  const entries = await readdir(staticDirectory, { withFileTypes: true });

  return entries
    .filter((entry) => entry.isDirectory() || (entry.isFile() && path.extname(entry.name) === ''))
    .map((entry) => entry.name)
    .filter(isUrlSlug);
};

const reservedSlugs = [
  ...new Set([...(await getTopLevelRouteSlugs()), ...(await getTopLevelStaticSlugs())])
].sort();

const output = `// Generated by scripts/generate-reserved-slugs.mjs.
// Do not edit this file directly.

export const reservedSuccessRoomSlugs = ${JSON.stringify(reservedSlugs, null, 2)} as const;
`;

await mkdir(path.dirname(outputPath), { recursive: true });
await writeFile(outputPath, output);

console.log(`Generated ${path.relative(projectRoot, outputPath)} with ${reservedSlugs.length} slugs.`);
